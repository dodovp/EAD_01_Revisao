package br.com.fiap.beans;

public class Disney {

	
	private int lucro;
	
	private String princesas;
	
	private boolean mickey;

	public int getLucro() {
		return lucro;
	}

	public void setLucro(int lucro) {
		this.lucro = lucro;
	}

	public String getPrincesas() {
		return princesas;
	}

	public void setPrincesas(String princesas) {
		this.princesas = princesas;
	}

	public Boolean getMickey() {
		return mickey;
	}

	public void setMickey(Boolean mickey) {
		this.mickey = mickey;
	}
	
	
}
